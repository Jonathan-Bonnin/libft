void *memchr(const void *s, int c, unsigned int n)
{
	int		i;
	char	*c_s;

	c_s = (char *) s;
	i = 0;
	while (c_s[i] != 0 && i < n)
	{
		if (c_s[i] == c)
			return &c_s[i];
		i++;
	}
	return (0);
}